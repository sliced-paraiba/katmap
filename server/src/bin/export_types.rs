use std::{error::Error, fs, path::PathBuf};

use katmap_server::types::{
    BreadcrumbPoint, ClientMessage, Maneuver, RouteLeg, ServerMessage, Waypoint,
};
use ts_rs::{Config, TS};

fn main() -> Result<(), Box<dyn Error>> {
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let output_path = manifest_dir
        .parent()
        .ok_or("server directory should have a project root parent")?
        .join("client/src/generated/types.ts");

    if let Some(parent) = output_path.parent() {
        fs::create_dir_all(parent)?;
    }

    let config = Config::new().with_large_int("number");
    let declarations = [
        export_decl::<Waypoint>(&config),
        export_decl::<BreadcrumbPoint>(&config),
        export_decl::<Maneuver>(&config),
        export_decl::<RouteLeg>(&config),
        export_decl::<ClientMessage>(&config),
        export_decl::<ServerMessage>(&config),
    ]
    .join("\n\n");

    fs::write(
        output_path,
        format!(
            "// Generated by `cargo run --manifest-path server/Cargo.toml --bin export-types`.\n\
             // Do not edit manually.\n\n\
             {declarations}\n"
        ),
    )?;

    Ok(())
}

fn export_decl<T: TS>(config: &Config) -> String {
    let decl = T::decl(config);
    if let Some(rest) = decl.strip_prefix("type ") {
        format!("export type {rest}")
    } else {
        format!("export {decl}")
    }
}
